// Insert admin blocks after sectionInfo in each language
// Pattern: after "quran_surahs: { title: '...', body: '...' }," followed by "  },"
const languages = ['ar', 'en', 'fr', 'de', 'tr', 'es', 'ur', 'id', 'ms', 'hi', 'bn', 'ru'];

for (const lang of languages) {
  const translation = adminTranslations[lang];
  if (!translation) {
    console.error(`Missing translation for ${lang}`);
    continue;
  }
  
  // Find the sectionInfo closing pattern for this language block
  // Look for the pattern: "  }," which closes sectionInfo, followed by "};" which closes the language const
  const langVarName = lang === 'id' ? 'id' : lang;
  
  // Use regex to find the right spot - after sectionInfo closing "  }," before "};" 
  // We need to find the pattern within each language block
  const blockStart = content.indexOf(`const ${lang}: TranslationKeys`);
  if (blockStart === -1) {
    console.error(`Could not find block for language: ${lang}`);
    continue;
  }
  
  // Find the "};" that closes this language block (searching from block start)
  let braceCount = 0;
  let blockEnd = -1;
  let foundFirstBrace = false;
  
  for (let i = blockStart; i < content.length; i++) {
    if (content[i] === '{') {
      braceCount++;
      foundFirstBrace = true;
    } else if (content[i] === '}') {
      braceCount--;
      if (foundFirstBrace && braceCount === 0) {
        blockEnd = i;
        break;
      }
    }
  }
  
  if (blockEnd === -1) {
    console.error(`Could not find block end for language: ${lang}`);
    continue;
  }
  
  // Find the last "  }," before blockEnd (the sectionInfo closing brace)
  const blockContent = content.substring(blockStart, blockEnd);
  const lastSectionClose = blockContent.lastIndexOf('  },');
  
  if (lastSectionClose === -1) {
    console.error(`Could not find sectionInfo close for language: ${lang}`);
    continue;
  }
  
  const insertPos = blockStart + lastSectionClose + 4; // after "  },"
  
  // Insert the admin block
  content = content.substring(0, insertPos) + '\n' + translation + '\n' + content.substring(insertPos);
  
  console.log(`✅ Added admin translations for: ${lang}`);
}

