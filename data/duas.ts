// دالة للحصول على فئة معينة
export const getDuaCategory = (id: string): DuaCategory | undefined => {
  return DUA_CATEGORIES.find(cat => cat.id === id);
};

// دالة للحصول على إجمالي عدد الأدعية
export const getTotalDuasCount = (): number => {
  return DUA_CATEGORIES.reduce((total, cat) => total + cat.duas.length, 0);
};

// دالة للحصول على ترجمة الدعاء حسب اللغة
export const getDuaTranslation = (dua: Dua, lang: SupportedLanguage): string => {
  return dua.translations[lang] || dua.translations.en;
};

// دالة للحصول على ترجمة المناسبة حسب اللغة
export const getDuaOccasionTranslation = (dua: Dua, lang: SupportedLanguage): string | undefined => {
  if (!dua.occasionTranslations) return dua.occasion;
  return dua.occasionTranslations[lang] || dua.occasionTranslations.en;
};

// دالة للحصول على اسم الفئة حسب اللغة
export const getDuaCategoryName = (category: DuaCategory, lang: SupportedLanguage): string => {
  return category.nameTranslations[lang] || category.nameTranslations.en;
};
