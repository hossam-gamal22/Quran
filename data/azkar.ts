// دالة للحصول على فئة معينة
export const getAzkarCategory = (id: string): AzkarCategory | undefined => {
  return AZKAR_CATEGORIES.find(cat => cat.id === id);
};

// دالة للحصول على إجمالي عدد الأذكار
export const getTotalAzkarCount = (): number => {
  return AZKAR_CATEGORIES.reduce((total, cat) => total + cat.azkar.length, 0);
};

// دالة للحصول على ترجمة الذكر حسب اللغة
export const getZikrTranslation = (zikr: Zikr, lang: SupportedLanguage): string => {
  return zikr.translations[lang] || zikr.translations.en;
};

// دالة للحصول على ترجمة الفائدة حسب اللغة
export const getZikrBenefitTranslation = (zikr: Zikr, lang: SupportedLanguage): string | undefined => {
  if (!zikr.benefitTranslations) return zikr.benefit;
  return zikr.benefitTranslations[lang] || zikr.benefitTranslations.en;
};

// دالة للحصول على اسم الفئة حسب اللغة
export const getCategoryName = (category: AzkarCategory, lang: SupportedLanguage): string => {
  return category.nameTranslations[lang] || category.nameTranslations.en;
};
