/**
 * Translate a benefit string from Arabic to the target language.
 * Returns the translated string if found, or the original Arabic if not.
 */
export function translateBenefit(arabicBenefit: string, language: string): string {
  if (!arabicBenefit) return arabicBenefit;
  if (language === 'ar') return arabicBenefit;
  
  const trimmed = arabicBenefit.trim();
  const translations = BENEFIT_MAP[trimmed];
  if (translations) {
    return translations[language] || translations.en || arabicBenefit;
  }
  
  // Fallback: return original Arabic
  return arabicBenefit;
}

/**
 * Check if a benefit string has a pre-translated version available.
 */
export function hasBenefitTranslation(arabicBenefit: string): boolean {
  if (!arabicBenefit) return false;
  return BENEFIT_MAP[arabicBenefit.trim()] !== undefined;
}
