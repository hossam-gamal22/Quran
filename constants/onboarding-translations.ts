// Fallback chain: requested language → en → ar
export function tOnboarding(key: string): string {
  const lang = getLanguage();
  return translations[lang]?.[key] ?? translations.en?.[key] ?? translations.ar[key] ?? key;
}

export function tOnboardingStep(step: number, total: number): string {
  const template = tOnboarding('stepOf');
  return template.replace('{step}', String(step)).replace('{total}', String(total));
}
